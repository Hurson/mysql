package com.avit.ads.requestads.bean;

public class UserInfoSync {
	
	private String locationCode;
	private String userTradeLevel;
	private String userSort;
	
	public String getLocationCode() {
		return locationCode;
	}
	public void setLocationCode(String locationCode) {
		this.locationCode = locationCode;
	}
	public String getUserTradeLevel() {
		return userTradeLevel;
	}
	public void setUserTradeLevel(String userTradeLevel) {
		this.userTradeLevel = userTradeLevel;
	}
	public String getUserSort() {
		return userSort;
	}
	public void setUserSort(String userSort) {
		this.userSort = userSort;
	}

}
